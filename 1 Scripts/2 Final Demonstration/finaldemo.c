void moveAround()
{
    motor[leftMotor] = rightMotorSpeed;
    motor[rightMotor] = -leftMotorSpeed;
}
void moveTo()
{
    motor[rightMotor] = rightMotorSpeed;
    motor[leftMotor] = leftMotorSpeed;
}
void moveBack()
{
    motor[rightMotor] = -rightMotorSpeed;
    motor[leftMotor] = -leftMotorSpeed;
}
void Stop()
{
    motor[leftMotor] = 0;
    motor[rightMotor] = 0;
}
